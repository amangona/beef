import SwiftUI

struct PointView: View {
    var point: Point?
    var body: some View {
        HStack {
            if point?.side == 1 {
                Spacer()
            }
            Text(point?.text ?? "Example point Text")
            .foregroundColor(.white)
            .font(.system(size: 12))
            .padding(20)
            .background(RoundedCorners(tl: 10, tr: 10, bl: self.point?.side == 0 ? 0 : 10 , br: self.point?.side == 1 ? 0 : 10).fill(Color.blue))
            if point?.side == 0 {
                Spacer()
            }
        }.padding()
        
    }
}

